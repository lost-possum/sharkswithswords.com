// for Loops
// repeat some code for a certain amount of time **********8

// for Loop can have 3 statements
// 1st statement declares and assign a variable
// 2nd is a condition

// count backwards from 10 to 1, print the results to console, then exits the for loop and prints "Happy new year!" to console
for(let i = 10; i > 0; i -= 1){                             
    console.log(i);
}
console.log("Happy new year!");

// counts from 1 to 10 then prints the results to console
for(let i = 1; i <= 10; i += 1){
    console.log(i);
}